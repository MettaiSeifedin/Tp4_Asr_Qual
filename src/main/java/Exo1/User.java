package Exo1;

public class User {
    private long id;
    private String name;

    // Getter et Setter pour l'ID
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    // Getter et Setter pour le nom
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

